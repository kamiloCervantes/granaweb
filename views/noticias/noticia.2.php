                <h2 class="page-header no-margin-top">Visita del Comité de Evaluadores Pares Internacionales de GRANA-OUI en la UANL</h2>
                <img src="/img/noticias/evento2.jpg" class="img-responsive imageborder" alt="Image" style="margin: 0 auto">
                 <p style="text-align: justify">
                                    Fue realizada la visita de pares evaluadores externos internacionales GRANA-OUI a la Facultad de Filosofía y Letras como fase para acreditar internacionalmente al programa académico de la Licenciatura en Historia y Estudios de Humanidades, esta visita fue realizada del 22 al 25 de marzo de 2015, como parte de la metodología aplicada fue realizada previamente un autoestudio de diagnóstico por parte del comité evaluador interno de la Facultad, los evaluadores externos llevaron a cabo entrevistas con estudiantes, profesores, empleadores, egresados y directivos asociados a esta carrera. El comité evaluador interno estuvo coordinado por la Dra. María Luisa Martínez Sánchez y la Mtra. Rocío Rodríguez. Así como, de la Dra. Ma. Guadalupe Rodríguez Blunes. En este proceso se destaca la participación de la Dirección de Acreditación y Evaluación Internacional de la UANL a cargo del Lic. Jorge Eugenio Ovalle de la Cruz. 
                                </p>
                                <p style="text-align: justify">
                                     El comité evaluador externo fue conformado por: la Dra. Catherine Vézina de la Universidad de Montreal, Canadá e Investigadora del CIDE, México; el Dr. Matthew Butler de la Universidad de Austin, Texas, Estados Unidos; el Dr. Víctor Álvarez de la Universidad de Antioquia, Colombia; la evaluadora nacional, la Mtra. María de los Ángeles Ancona, actual jefe de la Unidad de evaluación y acreditación de la a universidad de Guadalajara quien apoyo con asesoría asociada al contexto educativo nacional mexicano al comité evaluador externo. Así mismo, durante el proceso se contó con los apoyos del Dr. Donato Vallín González, director de GRANA-OUI. 
                                </p>
                                <p style="text-align: justify">
                                     GRANA-OUI, tiene su matríz en Montreal, Canadá; sus servidores en Phoenix, Arizona, Estados Unidos; sus programadores de sistemas en Córdova, Colombia; sus administradores en Guadalajara, Jalisco, México y más de 600 evaluadores externos en países de los 5 continentes. 
Al final de la visita se realizó la lectura del predictamen por parte de la Dra. Catherine Vézina como coordinadora del comité evaluador externo, destacando las fortalezas del programa académico evaluado. Este acto estuvo concurrido por profesores, estudiantes, directivos y egresados. 
Los resultados en extenso y la certificación serán entregados en un plazo no mayor a 30 días naturales después de esta visita.

                                </p>

