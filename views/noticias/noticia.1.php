                <h2 class="page-header no-margin-top">GRANA-OUI inicia procesos de evaluación en la UDG</h2>
                <img src="/img/noticias/evento1.jpg" class="img-responsive imageborder" alt="Image" style="margin: 0 auto">
                <p>
                    El viernes 22 de mayo de 2015 dieron inicio los procesos de 
                    evaluación para la acreditación internacional del programa 
                    GRANA-OUI en la Universidad de Guadalajara, México,  de 
                    dos Programas académicos en red: Médico Cirujano y Partero 
                    (Lic. en Medicina) ; Licenciado en Derecho. Además de 
                    programas académicos de los  Centros Universitarios 
                    Temáticos de Ciencias de la Salud y Ciencias 
                    Económico-Administrativas.
                </p>
                <p>
                    El acto inaugural fue realizado por la  Dra. Patricia Rosas 
                    Chávez, coordinadora de Innovación Educativa y Pregrado de 
                    la Universidad de Guadalajara (UDG).Este acto fue realizado 
                    en el auditorio del Centro Universitario de Ciencias de la 
                    Salud (CUCS) de la UDG.
                </p>
                <p>
                    Estuvieron presentes por la Universidad de Guadalajara: el 
                    Maestro Rogelio Zambrano Guzmán, secretario académico del 
                    CUCS, LA Maestra  María de Los Ángeles Ancona Valdéz Jefa 
                    de la Unidad de evaluación y acreditación de la CIEP de la 
                    UDG. Además de,  los profesores y directivos de ese centro 
                    universitario.  También participaron  por parte del 
                    programa GRANA-OUI: LA Dra. Norma Angélica Juárez  y el 
                    Dr. Donato Vallín. 
                </p>
                <p>
                   En este evento fueron presentada la importancia de la 
                   acreditación internacional , los procesos y procedimientos a 
                   realizar a las carreras  y  la caracterización del Programa 
                   de Evaluación-Certificación Internacional como:
                </p>
                
                <ul>
                    <li>Un modelo de evaluación con indicadores de segunda y 
                        tercera generación constantemente actualizado de acuerdo 
                        a estándares reconocidos internacionalmente.</li>
                    <li>Un proceso de evaluación y acompañamiento con la 
                        participación de expertos internacionales provenientes 
                        de diferentes regiones del globo.</li>
                    <li>La implicación del cuerpo académico, directivo y de 
                        otros actores  relevantes en el proceso de evaluación y 
                        en el diseño de estrategias y planes de mejoramiento 
                        continúo.</li>
                    <li>El desarrollo de capacidades internas para conducir los 
                        procesos de mejora que se requieran.</li>
                    <li>El uso de una plataforma en línea para conducir el 
                        proceso de evaluación, con recursos y herramientas que 
                        facilitan los procesos de evaluación interna y externa, 
                        que favorece los tiempos de evaluación, etapas de 
                        revisión y seguimiento a los planes de mejoramiento 
                        continuo.</li>
                    <li>Un proceso de evaluación-certificación acorde con los 
                        estándares internacionales y nacionales, lo cual permite 
                        a las IES avanzar en el reconocimiento de la calidad de 
                        sus programas, a nivel local e internacional, mejorar la 
                        visibilidad en los rankings de educación superior, y 
                        lograr una mayor pertinencia de sus programas para las 
                        necesidades de la sociedad. </li>
                    <li>Un instrumento de evaluación que propicia la movilidad 
                        profesional, la homologación de planes de estudios, la 
                        formación de redes académicas de investigación y 
                        desarrollo, y por ende el crecimiento social de los 
                        países latinoamericanos. </li>
                    <li>Una red de contactos con la comunidad internacional 
                        vinculada con el mejoramiento de la calidad de la 
                        educación superior. </li>
                </ul>
                
                <p>El proceso de evaluación de los programas académicos o 
                    educativos a evaluar en la Universidad de Guadalajara se 
                    desarrolla a través de las siguientes etapas: </p>
                
                <ul>
                    <li>Conformación del Comité de Evaluación Interna (CEI); </li>
                <li>Conformación del Comité de Evaluación Externa (CEE)</li>
                <li>Proceso de capacitación y formación de evaluadores</li>
                <li>Evaluación interna; </li>
                <li>Evaluación externa en línea;</li>
                <li>Visita a las Instalaciones de los evaluadores externos (visita de pares); </li>
                <li>Entrega de pre-dictamen a la Institución; </li>
                <li>Evaluación por parte de colegios especializados; </li>
                <li>Reporte-dictamen de resultados de la evaluación; </li>
                <li>Certificación y la recomendación del proceso de mejora permanente; </li>
                <li>Metaevaluación del proceso.</li>
                </ul>

                <p>
                    El proceso inicia con la integración de evaluadores internos conocida como CEI que tienen la función de integrar la  evaluación interna y se conforma por 2 directivos, 2 profesores y un estudiante del programa a evaluar. Posteriormente serán revisados por pares académicos a través de un proceso de evaluación externa. El programa también es valorado por miembros de diferentes sectores por disciplina (colegios especializados). Una vez realizadas estas valoraciones, el programa pasa a un consejo ejecutivo para dictaminar el nivel en que se encuentra el programa. 
                </p>
                
                <p>
                    La evaluación se realiza acorde a 10 rubros, a través de los cuales se integra y analiza toda la información cuantitativa y cualitativa del programa:
                </p>
                
                <ol>
                     <li>Impacto Social de la Formación Académica. </li>
                     <li>Resultados de la Investigación vinculados a la Formación Académica. </li>
                     <li>Ingreso, Permanencia y Eficiencia Terminal en la Formación Académica.</li> 
                     <li>Profesores Vinculados a la Formación Académica. </li>
                     <li>Pertinencia del Modelo Educativo y Estructura Curricular.</li>
                     <li>Estrategias Metodológicas de Aprendizaje. </li>
                     <li>Infraestructura, Equipamiento, Tecnologías y Bibliografía en la Formación Académica. </li>
                     <li>Impacto de las Actividades de Extensión, Vinculación y Difusión en la Formación Académica. </li>
                     <li>Reconocimiento Internacional de la Formación Académica. </li>
                     <li>Impacto en la Pertinencia de la Normatividad, la Administración y las Finanzas como Facilitadoras en la Formación Académica. </li>

                </ol>
                
                <p>
                    Cada rubro contiene a su vez 10 ítems, totalizando 100 ítems y 1.000 indicadores de segunda y tercera generación, bajo una evaluación principalmente cualitativa y referenciada, soportada y justificada con una evaluación cuantitativa que se circunscribe a documentación de apoyo para los efectos de su constatación y verificación. 
                </p>
                
                <p>
                    Posterior a este evento se realizaron visitas al Centro Universitario de Ciencias Económico-Administrativas  donde fueron  presentados los objetivos de este proceso ante directivos y profesores. Así mismo, se tuvo reunión con los coordinadores de carrera y secretarios académicos de centros universitarios de la UDG que ofrecen el programa en red  de Licenciatura en Derecho, el evento fue realizado en las oficinas de  la Coordinación de Innovación Educativa de la UDG. 
                </p>
                
                <p>
                    Se mencionó la importancia de estos procesos en la vida institucional para la Universidad de Guadalajara.
                </p>

