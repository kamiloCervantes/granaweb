                <h2 class="page-header no-margin-top">Foro Académico Mundial de Derechos Humanos</h2>
                <img src="/img/slide-1.png" class="img-responsive imageborder" alt="Image" style="margin: 0 auto">
                 <p style="text-align: justify">
El Foro Académico Mundial de Derechos Humanos (FAMDH) http://forofamdh.org es

un espacio de reflexión, análisis y discusión sobre los Derechos Humanos asociados a la educación

superior en el mundo, particularmente en Latinoamérica, para el mejoramiento de su calidad y

pertinencia formativa.                </p>                               
                <p style="text-align: justify">
El FAMDH en su edición 2017 se realiza en el marco de la feria Internacional del Libro

de Guadalajara -2017, https://www.fil.com.mx es organizado por la Universidad de Guadalajara

www.udg.mx a través del Centro Universitario de Ciencias Económico Administrativas (CUCEA)

www.udg.cucea.mx en colaboración con The Organization Generation of Resources for

Accreditation in Nations of the America (GRANA) www.certrification-grana.org el Centro de

Estudios e Investigaciones para el Desarrollo Docente A.C. (CENID), http://cenid.org.mx con la

participación de 155 Instituciones de Educación Superior y 61 Organizaciones Internacionales del

Hemisferio. Se centra en un espacio de supra articulación entre la formación universitaria asociada

a los derechos humanos como necesidad social mundial. La FAMDH es una aldea global de

cooperación y aprendizaje donde académicos y expertos debaten la pertinencia y la calidad en la

formación universitaria haciendo sinergia con diversos sectores de la sociedad en el diseño y

criterios consensuados con la organización de programas académicos y desarrollo de estrategias

multidireccional en instituciones y sectores sociales.                </p>  
                <p style="text-align: justify">
El FAMDH, cuenta con una plataforma informática y de contenido que permite la

interacción ágil y expedita en forma permanente a educadores y líderes en derechos humanos,

instituciones educativas, organizaciones gubernamentales y ONGs para formar una colaboración

fructífera global a largo plazo, además de involucrarse con la comunidad internacional de educación

en derechos humanos.
                <p style="text-align: justify">
Dentro de los objetivos del FAMDH 2017 es impulsar la cooperación académica a nivel

internacional entre los participantes mediante comunidades de cooperación y aprendizaje duraderas

en la educación de derechos humanos con los siguientes programas y actividades sistemáticas:
                
                 <ul>
                    <li> Plataformas informáticas y MOC ́s</li>
<li>Conferencias Magistrales</li>
<li>Ciclo de conferencias</li>
<li>Sesiones especiales (Paneles, talleres breves)</li>
<li>Presentaciones de ponencias paralelo</li>
<li>Presentación del Libros</li>
<li>Declaratoria académica de Derechos Humanos</li>
                </ul>
                <p style="text-align: justify">
Las actividades a realizarse serán el 28 y 29 de noviembre del año en curso en la ciudad de

Guadalajara, Jalisco, México, en el marco de la feria internacional del libro de Guadalajara edición

2017.               </p>
                <br/>
