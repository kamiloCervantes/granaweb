                 <p style="text-align: justify">
                     Un equipo de maestras, maestros y administrativos de nuestra Facultad se dieron cita
                     en instalaciones de la UANL,  para participar en el “Taller de evaluación con fines de 
                     re acreditación Internacional del Programa Educativo de la Licenciatura en Ciencias de 
                     la Comunicación” a cargo del organismo GRANA International Evaluation and Certification.
                 </p>                               
                <p style="text-align: justify">
                    El  Coordinador, M.C. Francisco G. Valdez Rincón agradeció el esfuerzo de los 
                    participantes, ya que forma parte de los trabajos que permitirán mantener a nuestra Facultad
                    a la vanguardia en el estudio y enseñanza de las ciencias de la comunicación con un programa 
                    educativo certificado de calidad mundial, también al Dr. Donato Vallín González , Director 
                    de GRANA Internacional por la impartición del taller, al Dr. José Nicolás Barragán Codina y
                    al Mtro. Miguel Ángel Meza de León, colaboradores de la Dirección de Acreditación y 
                    Evaluación Internacional de la UANL por su invaluable apoyo en la búsqueda de este 
                    importante objetivo. </p>
