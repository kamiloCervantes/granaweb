                <h2 class="page-header no-margin-top">Entrega de la Certificación Internacional  de GRANA-OUI al Programa Académico de la Licenciatura en Historia y Estudios de Humanidades de la Universidad Autónoma de Nuevo León, México</h2>
                <img src="/img/noticias/evento3.jpg" class="img-responsive imageborder" alt="Image" style="margin: 0 auto">
                 <p style="text-align: justify">
En el proceso de internacionalización que se vive en la Universidad Autónoma de Nuevo León, la Facultad de Filosofía y Letras (FFYL) tiene un compromiso evidente: obtuvo dos acreditaciones con este carácter, para sus programas de Ciencias del Lenguaje e Historia y Estudios Humanísticos.                                
                </p>                               
                <p style="text-align: justify">
Este logro institucional, encabezado por la doctora María Luisa Martínez Sánchez, coloca a la FFYL como la única dependencia de la Máxima Casa de Estudios en tener cuatro programas educativos certificados por una agencia internacional; récord que no se encuentra en instituciones de educación superior en el país.                               
                </p>  
                <p style="text-align: justify">
En ceremonia que se realizó el 30 de abril, la doctora Elizabeth Hermes, representante de Avalag y el doctor Donato Vallín González, de GRANA-OUI entregaron la acreditación de calidad educativa por parte de cada agencia al doctor Daniel González Spencer, Secretario de Relaciones Internacionales UANL, quien encabezó este acto en representación del Rector Jesús Ancer Rodríguez.estuvieron presentes entre otras autoridades:                </p>  
                <ul>
                    <li> El Dr. Jose Manuel Alcocer González, secretario académico de la UANL.</li>
<li>La Dra. María Luisa Martínez Sánchez, Directora de la Facultad de Filosofía y Letras de la UANL.</li>
<li>El  Lic. Jorge Ovalle de la Cruz, Director de Acreditación y Evaluación Internacional.</li>
<li>La Dra. Maria Guadalupe Blunes, Directora de Evaluación Institucional de la UANL.</li>
<li>Profesores heméritos, decanos, estudiantes y profesores de la facultad</li>
<li>Invitados especiales.</li>
                </ul>
 

